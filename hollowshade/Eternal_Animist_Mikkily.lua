function event_signal(e)
	if e.signal==1 then
		e.self:Say("Aye, it is grand to finally meet the one we've had so many hopes for o'er many long hours. Just when we'd worry about something, there ye be, taking care of the problem. You are like so many of us -- willing to learn, determined, and ready to face any challenge. Grand day indeed, aye. I was one of the first barbarians to learn the way of the wolf and to bring it to my side.");
	end
end